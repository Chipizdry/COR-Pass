        periodElem.style.height = `${fixedHeight}px`;
        
        // –†–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏–µ –æ—Ç –Ω–∏–∑–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –ø–æ—Ä—è–¥–∫–æ–≤–æ–≥–æ –Ω–æ–º–µ—Ä–∞
